CREATE TABLE benchmark_exercise (
    id INTEGER PRIMARY KEY GENERATED BY DEFAULT AS IDENTITY,
    exercise_name TEXT NOT NULL,
    date_created TIMESTAMPTZ DEFAULT now() NOT NULL,
    date_modified TIMESTAMPTZ,
    workout_id INTEGER
        REFERENCES benchmark_workout(id) ON DELETE CASCADE NOT NULL,
    user_id INTEGER
        REFERENCES benchmark_user(id) ON DELETE CASCADE NOT NULL
);