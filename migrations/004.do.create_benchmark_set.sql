CREATE TABLE benchmark_set (
    id INTEGER PRIMARY KEY GENERATED BY DEFAULT AS IDENTITY,
    set_weight INTEGER NOT NULL,
    set_repetition INTEGER NOT NULL,
    date_created TIMESTAMPTZ DEFAULT now() NOT NULL,
    date_modified TIMESTAMPTZ,
    exercise_id INTEGER
        REFERENCES benchmark_exercise(id) ON DELETE CASCADE NOT NULL,
    user_id INTEGER
        REFERENCES benchmark_user(id) ON DELETE CASCADE NOT NULL
);